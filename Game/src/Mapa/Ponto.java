package trabalhofinal.Mapa;

/**
 *
 * @author henrique
 */
public class Ponto {
    private int posx;
    private int posy;

    public Ponto(int posx, int posy) {
        this.posx = posx;
        this.posy = posy;
    }

    public int getPosx() {
        return posx;
    }

    public void setPosx(int posx) {
        this.posx = posx;
    }

    public int getPosy() {
        return posy;
    }

    public void setPosy(int posy) {
        this.posy = posy;
    }
    
}
